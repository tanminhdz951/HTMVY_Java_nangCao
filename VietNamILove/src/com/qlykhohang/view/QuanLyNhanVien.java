    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if (nameTextField.getText().equals("") == true) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập tên nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (nameCheck(nameTextField.getText(), emSer.getAllNhanVien()) == false) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng không nhập trùng tên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (telTextField.getText().equals("") == true) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập số điện thoại nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (telCheck(telTextField.getText()) == false) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Số điện thoại không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (addressTextField.getText().equals("") == true) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập địa chỉ nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (passTextField.getText().equals("") == true) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập mật khẩu nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            em = new NhanVien();
            em.setTenNV(nameTextField.getText());
            em.setSoDienThoai(telTextField.getText());
            em.setDiaChi(addressTextField.getText());
            em.setMatKhau(passTextField.getText());
            try {
                emSer.addNhanVien(em);
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Thất bại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int row = emTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng chọn nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            if (nameTextField.getText().equals("") == true) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập tên nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else if (nameCheck(nameTextField.getText(), emSer.getAllNhanVien()) == false) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng không nhập trùng tên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else if (telTextField.getText().equals("") == true) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập số điện thoại nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else if (telCheck(telTextField.getText()) == false) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Số điện thoại không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else if (addressTextField.getText().equals("") == true) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập địa chỉ nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else if (passTextField.getText().equals("") == true) {
                JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Vui lòng nhập mật khẩu nhân viên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } else {
                em = new NhanVien();
                em.setTenNV(nameTextField.getText());
                em.setSoDienThoai(telTextField.getText());
                em.setDiaChi(addressTextField.getText());
                em.setMatKhau(passTextField.getText());
                em.setMaNV(idTextField.getText());
                try {
                    emSer.updateNhanVien(em);
                    JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(QuanLyNhanVien.this, "Thất bại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_editButtonActionPerformed

