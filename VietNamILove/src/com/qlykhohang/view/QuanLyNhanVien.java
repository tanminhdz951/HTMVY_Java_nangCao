    private void setTableData(List<NhanVien> ems) {
        for (NhanVien e : ems) {
            dfTableModel.addRow(new Object[]{e.getMaNV(), e.getTenNV(), e.getSoDienThoai(), e.getDiaChi(), e.getMatKhau()});
        }
    }

    private boolean telCheck(String tel) {
        if (tel.length() != 10) {
            return false;
        } else {
            for (int i = 0; i < tel.length(); ++i) {
                char c = tel.charAt(i);
                if (c < '0' || c > '9') {
                    return false;
                }
            }
        }
        return true;
    }

    private boolean nameCheck(String name, List<NhanVien> ems) {
        for (NhanVien e : ems) {
            if (name.equals(e.getTenNV())) {
                return false;
            }
        }
        return true;
    }

